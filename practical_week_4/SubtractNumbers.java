/*Week 4 - example to show WHILE loop 
print the number 10 up to 1 on the screen */

public class SubtractNumbers
{
   public static void main(String []args)
   {
      //declare counter variable
      int number =10;
      while (number >= 1)
      {
         System.out.println(number);
         number--;//decrease 1 from variable counter each time(counter stops in = 0)
      
      }//closes loop
   }//end of main method
}//end of main class