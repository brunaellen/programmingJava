/*Week 4 - example to show WHILE loop 
print Welcome to java 100 times to scrren */

public class WhileExample1
{
   public static void main(String []args)
   {
      //declare counter variable
      int counter = 1;
      while (counter <= 10)
      {
         System.out.println("Welcome to java");
         counter++;//adds 1 to variable counter each time(counter stops in =101)
   
      }//closes loop
   }//end of main method
}//end of main class