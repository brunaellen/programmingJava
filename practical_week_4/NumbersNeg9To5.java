/*Week 4 - Practical4A -Question3
Write a program, using a while loop, that will display 
the numbers -9 to 5 on screen.
 */

public class NumbersNeg9To5
{
   public static void main(String []args)
   {
      //declare counter variable
      int number = -9;
      while (number <= 5)
      {
         System.out.println(number);
         number++;
         
      }//end of loop
   }//end of main method
}//end of main class