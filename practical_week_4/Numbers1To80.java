/*Week 4 -  Practical4A -Question2
example to show WHILE loop 
print the number 1 to 80 on the screen */

public class Numbers1To80
{
   public static void main(String []args)
   {
      //declare counter variable
      int number =1;
      while (number <= 80)
      {
         System.out.println(number);
         number++;//adds 1 to variable counter each time(counter stops in = 11)

      }//end of loop
   }//end of main method
}//end of main class