/*Week 4 -  Practical4A -Question1
example to show WHILE loop 
print the number 1 to 10 on the screen */

public class Numbers
{
   public static void main(String []args)
   {
      //declare counter variable
      int number = 0;
      
      while (number < 10)
      {
         number++;
       //adds 1 to variable counter each time(counter stops in = 11)
         System.out.println(number);

      }//end of loop
   }//end of main method
}//end of main class